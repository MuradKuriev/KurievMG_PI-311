nums.append(int(input("Введите число 4: ")))
nums.append(int(input("Введите число 5: ")))

# все ли уникальны
all_unique = True
i = 0
while i < len(nums):
    j = i + 1
    while j < len(nums):
        if nums[i] == nums[j]:
            all_unique = False
        j = j + 1
    i = i + 1

if all_unique:
    print("Все числа уникальные")
else:
    print("Есть одинаковые числа")

# количество чётных
even_count = 0
# количество отрицательных
neg_count = 0
# количество в промежутке [-256; 1024]
range_count = 0

