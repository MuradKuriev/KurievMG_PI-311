"""5. Функция is_prime (простое число)

"""

def is_prime(n):
    if n <= 1:
        return False

    i = 2
    while i * i <= n:
        if n % i == 0:
            return False
        i = i + 1

    return True

num = int(input("Введите число: "))
print(is_prime(num))

